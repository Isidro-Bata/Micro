package model.value_object;
public class Ativo {
    private float saldo, emp, liq;

    public float getSaldo() {
        return saldo;
    }

    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }

    public float getEmp() {
        return emp;
    }

    public void setEmp(float emp) {
        this.emp = emp;
    }

    public float getLiq() {
        return liq;
    }

    public void setLiq(float liq) {
        this.liq = liq;
    }
    
    
}
